class UserEvent {
  String docId;
  String id;
  String name;
  String ph;
  String fee;
  String eventName;
  String cnic;
  String email;
  String status;
  int food_count;

  UserEvent({
    required this.docId,
    required this.id,
    required this.name,
    required this.ph,
    required this.fee,
    required this.eventName,
    required this.cnic,
    required this.email,
    required this.status,
    required this.food_count,
  });
}
